#!/usr/bin/env node
// Emits public/native-shell/server-config.js with the URL the Capacitor
// shell should retry against when the WebView fails to reach the dev /
// production server. Resolves at build time from the same env precedence
// capacitor.config.ts uses, so dev simulators talk to 127.0.0.1:3000 and
// production builds talk to the real domain.
//
// Run automatically as part of `npm run cap:sync`. Safe to run by hand:
//   CAPACITOR_SERVER_URL=https://app.athletixos.com node scripts/native-shell-config.mjs

import fs from "node:fs";
import path from "node:path";
import url from "node:url";

const HERE = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, "..");

const DEFAULT = "http://127.0.0.1:3000";
const resolved =
  process.env.CAPACITOR_SERVER_URL ||
  process.env.NEXT_PUBLIC_APP_URL ||
  DEFAULT;

// Strip trailing slash so consumers can do `${SERVER_URL}/member` without
// risking `//member`.
const normalized = resolved.replace(/\/$/, "");

const out = path.join(ROOT, "public", "native-shell", "server-config.js");
const body =
  `// AUTOGENERATED by scripts/native-shell-config.mjs — do not hand-edit.\n` +
  `// Regenerated by \`npm run cap:sync\`. Source env: ` +
  `${process.env.CAPACITOR_SERVER_URL ? "CAPACITOR_SERVER_URL" : process.env.NEXT_PUBLIC_APP_URL ? "NEXT_PUBLIC_APP_URL" : "default"}.\n` +
  `window.NATIVE_SERVER_URL = ${JSON.stringify(normalized)};\n`;

fs.writeFileSync(out, body);
console.log(`[native-shell-config] wrote ${path.relative(ROOT, out)} → ${normalized}`);

// Loud warning if no env was set AND the resolved URL points at the dev
// fallback. `server-config.js` is now gitignored so the only way it can
// reach a release bundle is via this script; if a release engineer ran
// us without setting CAPACITOR_SERVER_URL, the native shell would auto-
// retry against 127.0.0.1:3000 on a real device. Surface that NOW.
if (
  !process.env.CAPACITOR_SERVER_URL &&
  !process.env.NEXT_PUBLIC_APP_URL &&
  normalized === DEFAULT
) {
  console.warn(
    `[native-shell-config] WARNING: neither CAPACITOR_SERVER_URL nor ` +
      `NEXT_PUBLIC_APP_URL is set — using dev fallback (${DEFAULT}). ` +
      `Fine for simulator dev. For a release build, set CAPACITOR_SERVER_URL ` +
      `to the production app URL before running \`npm run cap:sync\`.`,
  );
}
